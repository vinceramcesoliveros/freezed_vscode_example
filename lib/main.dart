class MyHomePage extends StatefulWidget {
  const MyHomePage({super.key, required this.title});

  // This widget is the home page of your application. It is stateful, meaning
  // that it has a State object (defined below) that contains fields that affect
  // how it looks.

  // This class is the configuration for the state. It holds the values (in this
  // case the title) provided by the parent (in this case the App widget) and
  // used by the build method of the State. Fields in a Widget subclass are
  // always marked "final".

  final String title;

  @override
  State<MyHomePage> createState() => _MyHomePageState();
}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;
  final exampleFreezed = ExampleFreezed.fromJson({'id': '1', 'name': 'test'});
  final exampleJsonSerializable =
      ExampleJsonSerializable.fromJson({'id': '1', 'name': 'test'});
  final example = Example.fromMap({'id': '1', 'name': 'test'});

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  @override
  void initState() {
    super.initState();
  }

  @override
  Widget build(BuildContext context) {
    /// add a breakpoint here and inspect the value of example variable
    // Expected output:
    //   id: "1"
    //   name: "test"
    //
    // Actual result:
    //   <error>: <getObject: Unexpected error from chrome devtools:>
    return Scaffold(
      appBar: AppBar(
        title: Text(widget.title),
      ),
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            Text(
              'You have pushed the button this many times: ${example.name}',
            ),
            Text(
              'You have pushed the button this many times: ${exampleFreezed.name}',
            ),
            Text(
              'You have pushed the button this many times: ${exampleJsonSerializable.name}',
            ),
            Text(
              '$_counter',
              style: Theme.of(context).textTheme.headlineMedium,
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _incrementCounter,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ),
    );
  }
}
